<h1>About this site</h1>

<p>This site has one data type defined in the static/admin/config.yml called things.</p>

<p>There is also a page that creates a list of things to be consumed for the home page in /src/routes/things.json.js. It basically takes all the documents in the /src/things folder and sums them up so you can prsent a list of things like we do on the <a href="/">home</a> page.</p> 

<p>The admin takes you to the login for the netlify cms, make sure you've jumped through all the hoops needed to set up the CMS on your deployment.</p>